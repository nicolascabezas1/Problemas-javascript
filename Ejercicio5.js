// Inicializar la variable para almacenar la suma
var suma = 0;

// Bucle for para iterar sobre los números pares entre 50 y 200
for (var i = 50; i <= 200; i += 2) {
    // Sumar el número par a la variable suma
    suma += i;
}

// Imprimir la suma total
console.log("La suma de los números pares entre 50 y 200 es: " + suma);
